import math

result = math.floor(25.90)
print(result)

result = math.ceil(25.90)
print(result)

result = math.pi
print(result)

# logaritmo neperiano
result = math.log(25, 5)
print(result)

# Tangente
result = math.tan(25)
print(result)

# Coseno
result = math.cos(25)
print(result)

# Raiz de pi
raiz_pi = math.sqrt(math.pi)
print(raiz_pi)

factor = math.factorial(7)
print(factor)
